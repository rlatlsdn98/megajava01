package my06;

public class 반복문 {

	public static void main(String[] args) {
		
		// while 문으로 10 부터 1까지 출력
		// 반복문 : 초기값, 조건문, 증감식 필요!
		int count = 11;
		while (count > 1) {
			count--; // count = count - 1
			System.out.print(count + " ");
			
			
			
		}
		// for 문으로 1 부터 10까지 출력
		for (int i = 1; i < 11; i++) {
			System.out.print(i + " ");
			
		}
			
		

	}

}
