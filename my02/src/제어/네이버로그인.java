package 제어;

public class 네이버로그인 {

	public static void main(String[] args) {
		
		int id = 1111;
		// 비교 연산자는 조건에 사용되고, 결과는 무조건 boolean이다.
		if( id == 2222 ) { // 소괄호 안 조건이 맞으면(true) , 중괄호에 있는 코드 실행!
			System.out.println("로그인ok...!!!");			
		}else { // 조건이 안맞으면  , 중괄호에 있는 코드 실행!
			System.out.println("로그인fail...!!!");
		}

	}

}
