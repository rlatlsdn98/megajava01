package 데이터;

public class 기본데이터2 {

	public static void main(String[] args) {
		// 기본 데이터 저장
		// 정수, 실수 저장
		
		int x = 250; // int는 -21~21억까지 사용 가능
		double y = 5.5; // 실수 
		char z = '한'; //한글자인경우 '(작은따음표)
		boolean a = true; // true:참  false:거짓
		
		
		
		System.out.println("x:"+x);
		System.out.println("y:"+y);
		System.out.println("z:"+z);
		System.out.println("a:"+a);
	
		

	}

}
